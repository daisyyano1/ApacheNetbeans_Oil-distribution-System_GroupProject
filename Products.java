    private void addproductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addproductActionPerformed
        // TODO add your handling code here:
        try {
        // 1. Read values from textfields
        String name = productname.getText();
        String desc = description.getText();
        double pr = Double.parseDouble(price.getText());
        int stock = Integer.parseInt(stockquantity.getText());
        int reorder = Integer.parseInt(reorderlevel.getText());

        // 2. Connect to database
        java.sql.Connection conn = java.sql.DriverManager.getConnection(
            "jdbc:mysql://localhost/oildb", "root", ""
        );

        // 3. Insert query
        String sql = "INSERT INTO products(productname, description, price, stockquantity, reorderlevel) VALUES (?, ?, ?, ?, ?)";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, name);
        pst.setString(2, desc);
        pst.setDouble(3, pr);
        pst.setInt(4, stock);
        pst.setInt(5, reorder);

        pst.executeUpdate();
        conn.close();

        javax.swing.JOptionPane.showMessageDialog(this, "Product Added!");

        loadProducts();  // refresh table
        
        // ⭐⭐⭐ CLEAR TEXTFIELDS HERE ⭐⭐⭐
        productid.setText("");
        productname.setText("");
        description.setText("");
        price.setText("");
        stockquantity.setText("");
        reorderlevel.setText("");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
        
    }//GEN-LAST:event_addproductActionPerformed

