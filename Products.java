    private void loadProducts() { //loads data from the database
        try {
        // 1. Connect to database
        java.sql.Connection conn = java.sql.DriverManager.getConnection(
            "jdbc:mysql://localhost/oildb", "root", ""
        );

        // 2. Query the database
        String sql = "SELECT * FROM products";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        // 3. Prepare table model
        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) ptable.getModel();

        // 4. Clear existing rows
        model.setRowCount(0);

        // 5. Add rows from the database to the table
        while (rs.next()) {
            Object row[] = {
                rs.getInt("productid"),
                rs.getString("productname"),
                rs.getString("description"),
                rs.getDouble("price"),
                rs.getInt("stockquantity"),
                rs.getInt("reorderlevel")
            };
            model.addRow(row);
        }

        conn.close();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }
