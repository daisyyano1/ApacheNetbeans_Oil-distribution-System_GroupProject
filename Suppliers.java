    private void stableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stableMouseClicked
        // TODO add your handling code here:
        // Get the model and the index of the selected row
        DefaultTableModel model = (DefaultTableModel) stable.getModel();
        int selectedRowIndex = stable.getSelectedRow();
        
        //if a row is actually selected
        if (selectedRowIndex != -1) {
            // Read the values from the selected table row
            String id = model.getValueAt(selectedRowIndex, 0).toString();
            String name = model.getValueAt(selectedRowIndex, 1).toString();
            String loc = model.getValueAt(selectedRowIndex, 2).toString();
            String contact = model.getValueAt(selectedRowIndex, 3).toString();
            String phoneNum = model.getValueAt(selectedRowIndex, 4).toString();
            String mail = model.getValueAt(selectedRowIndex, 5).toString();
             
            // Set the text fields with the selected data
            supplierid.setText(id);
            suppliername.setText(name);
            location.setText(loc);
            contactperson.setText(contact);
            phone.setText(phoneNum);
            email.setText(mail);

        }
    }//GEN-LAST:event_stableMouseClicked

