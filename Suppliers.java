    private void addsuppliersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addsuppliersActionPerformed
        // TODO add your handling code here:
        try {
        String name = suppliername.getText();
        String loc = location.getText();
        String contact = contactperson.getText();
        String ph = phone.getText();
        String em = email.getText();

        Connection con = getConnection();
        PreparedStatement ps = con.prepareStatement(
            "INSERT INTO suppliers (suppliername, location, contact, phone, email) VALUES (?,?,?,?,?)");
        ps.setString(1, name);     // Corresponds to suppliername
        ps.setString(2, loc);      // Corresponds to location
        ps.setString(3, contact);  // Corresponds to contactperson
        ps.setString(4, ph);       // Corresponds to phone
        ps.setString(5, em);       // Corresponds to email
        
        ps.executeUpdate();
        con.close();

        JOptionPane.showMessageDialog(null, "Supplier added successfully!");

        // Clear fields
        supplierid.setText("");
        suppliername.setText("");
        location.setText("");
        contactperson.setText("");
        phone.setText("");
        email.setText("");

        loadSuppliers();

    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_addsuppliersActionPerformed

    private void editsuppliersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editsuppliersActionPerformed
        // TODO add your handling code here:
        String idText = supplierid.getText().trim();
        
        // 1. Validate the SupplierID field
        if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Supplier ID to edit.", "Input Error", JOptionPane.WARNING_MESSAGE);
        return; // Stop execution if validation fails
    }
        try { 
        int id = Integer.parseInt(idText);
        String name = suppliername.getText();
        String loc = location.getText();
        String contact = contactperson.getText();
        String ph = phone.getText();
        String em = email.getText();

        Connection con = getConnection();
        PreparedStatement ps = con.prepareStatement(
            "UPDATE suppliers SET suppliername=?, location=?, contact=?, phone=?, email=? WHERE supplierid=?"
        );
        ps.setString(1, name);
        ps.setString(2, loc);
        ps.setString(3, contact);
        ps.setString(4, ph);
        ps.setString(5, em);
        ps.setInt(6, id);
        
        ps.executeUpdate();
        con.close();

        JOptionPane.showMessageDialog(null, "Supplier updated successfully!");

        // Clear fields
        supplierid.setText("");
        suppliername.setText("");
        location.setText("");
        contactperson.setText("");
        phone.setText("");
        email.setText("");

        loadSuppliers();

    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_editsuppliersActionPerformed

    private void deletesuppliersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletesuppliersActionPerformed
        // TODO add your handling code here:
        String idText = supplierid.getText().trim();
    
    // 1. Check if the ID field is empty (meaning no row was selected)
    if (idText.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a supplier from the table to delete.", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return; 
    }
    try {
        int id = Integer.parseInt(idText);

        // 2. Confirmation dialog
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete supplier ID " + id + "?", 
            "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            
            // --- 3. Database Deletion ---
            // Use the getConnection() method you already have for consistency
            java.sql.Connection conn = getConnection(); 
            if (conn == null) return; // Exit if connection failed
    
        String sql = "DELETE FROM suppliers WHERE supplierid=?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        pst.setInt(1, id);

        pst.executeUpdate();
        conn.close();

        javax.swing.JOptionPane.showMessageDialog(this, "Supplier Deleted!");
        loadSuppliers();

        // Clear fields
        supplierid.setText("");
        suppliername.setText("");
        location.setText("");
        contactperson.setText("");
        phone.setText("");
        email.setText("");
        }
    } 
        catch(java.lang.NumberFormatException nfe) {
        // Safety catch if the user manually tampered with the ID field
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Invalid Supplier ID format.", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
      }
    }//GEN-LAST:event_deletesuppliersActionPerformed

